// A program to demonstrate bitwise operator

package org.tnsif.operators;

public class BitwiseOperatorDemo {

	public static void main(String[] args) {
		int x = 13;
		int y = 9;
		
		System.out.println(x&y);
		System.out.println(x|y);
		System.out.println(x^y);
		
		
		System.out.println(8>>1);
		System.out.println(8<<1);

	}

}
