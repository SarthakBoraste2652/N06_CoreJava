//A program to demonstrate logical operators

package org.tnsif.operators;

public class LogicalOperatorsDemo {

	public static void main(String[] args) {
		System.out.println(31 != 7 && 3>=6);
		System.out.println(31 != 7 || 3>=6);
		System.out.println(!(31 != 7 || 3>=6));
		System.out.println(!(31 != 7 && 3>=6));
	
	}

}
